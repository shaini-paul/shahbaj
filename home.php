	function UpdatePcbPrices()
	{
		ClearPrices();
		
		var req = new XMLHttpRequest();
		req.onreadystatechange = function() {
			if (req.readyState == 4) {
				// s* if request is succesffull then parse prices will fetch the data and distribute among component
				if(req.status == 200)
				{
					ParsePrices(req.responseText);
				}
				else
				{
					ShowError("בקשת עדכון המחירים לא הצליחה." + "<br>" + "דווחו זאת למנהל האתר!");
				}
			}
		};

		var params = "";
		params += InputValue('panel_type');
		params += InputSelectValue('board_width');
		params += InputSelectValue('board_height');
		params += InputValue('layers');
		params += InputSelectValue('quantity');
		params += InputValue('thickness');
		params += InputValue('mask_color');
		params += InputValue('text_color');
		params += InputValue('finishing');
		params += InputValue('trace_width');
		params += InputValue('drill_size');
		params += InputValue('copper_thickness');
		params += InputValue('inner_copper_thickness');
		params += InputSelectValue('vslots');
		params += InputValue('stamp_holes');
		params += InputValue('blind_burried_vias');
		params += InputSelectValue('testing');
		params += InputSelectValue('manufacturing_time');
		params += InputSelectValue('delivery_type');
		params += 'end=end';
		// s* sending post request to php page named pcb_prices.php
		
		req.open("POST", "https://www.4project.co.il/pcb_prices.php?" + params, true);
		req.send();
	}

