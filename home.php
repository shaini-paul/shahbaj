	function ParsePrices(str)
	{
		// s* this is parsing the json reqsponse into javascript object
		var result = JSON.parse(str);
		
		if(result.ErrorMessage != '' && result.InfoMessage != '')
		{
			ShowError(result.ErrorMessage + '<br>' + result.InfoMessage);
			return;
		}

		if(result.ErrorMessage != '')
		{
			ShowError(result.ErrorMessage);
			return;
		}
		
		if(result.InfoMessage != '')
		{
			ShowError(result.InfoMessage);
		}
		// *update price is function to update price from response data

		
		UpdatePrice('board_price', result.BoardPrice);
		UpdatePrice('mask_color_price', result.MaskColorPrice);
		UpdatePrice('finishing_price', result.FinishingPrice);
		UpdatePrice('drill_size_price', result.DrillSizePrice);
		UpdatePrice('copper_thickness_price', result.CopperThicknessPrice);
		UpdatePrice('vslots_price', result.VSlotPrice);
		UpdatePrice('stamp_holes_price', result.StampHolesPrice);
		UpdatePrice('testing_price', result.TestingPrice);
		UpdatePrice('manufacturing_time_price', result.ManufacturingTimePrice);
		UpdatePrice('delivery_type_price', result.DeliveryTypePrice);
		UpdatePrice('customs_price', result.CustomsPrice);
		UpdatePrice('total_price', result.TotalPrice);

		document.getElementById("board_area").innerHTML = result.BoardArea;
		document.getElementById("weight").innerHTML = result.Weight;
		document.getElementById("volumetric_weight").innerHTML = result.VolumetricWeight;
		
		if(result.Weight < 2.0)
		{
			document.getElementById("delivery_type_registered").style.display = '';
		}
		else
		{
			document.getElementById("delivery_type_express").selected = true;
			document.getElementById("delivery_type_registered").style.display = 'none';
		}

		document.getElementById("addtocart").disabled = false;
		document.getElementById("addtocart").className = 'button add-to-cart-button order';
	}

